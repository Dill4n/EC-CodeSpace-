/* CSS, which stands for Cascading Style Sheets, CSS is a web development technology that allows developers to control the visual presentation of web pages.  

 With CSS, you can define styles for various elements on a webpage, such as fonts, colours, spacing, layout, and more. By separating the content (HTML) from its presentation (CSS), developers can create more flexible and maintainable websites. The term "cascading" in CSS refers to the way styles are applied in a hierarchy, allowing for a consistent and organised approach to styling.

 CSS enables web designers and developers to achieve a consistent look and feel across multiple pages or an entire website. It plays a crucial role in enhancing the user experience by controlling the visual aspects of a website and ensuring compatibility with different devices and screen sizes.

Example:

 index.html 
<!DOCTYPE html>
<html lang="en">
<head>
    <meta charset="UTF-8">
    <meta name="viewport" content="width=device-width, initial-scale=1.0">
    <title>Simple CSS Example with Classes</title>
    <link rel="stylesheet" href="styles.css">
</head>
<body>
    <header class="site-header">
        <h1>Welcome to My Website</h1>
    </header>
    <section class="main-section">
        <p class="intro-paragraph">This is a simple example of CSS styling with classes.</p>
    </section>
</body>
</html>

 
styles.css:

body {
    font-family: Arial, sans-serif;
    margin: 20px;
}

.site-header {
    background-color: #333;
    color: #fff;
    padding: 10px;
    text-align: center;
}

.site-header h1 {
    margin: 0;
}

.main-section {
    margin-top: 20px;
}

.intro-paragraph {
    color: #555;
    line-height: 1.5;
}

*/
