/* <!DOCTYPE html>

Description: Declares the HTML document type and version.
<html>

Description: The root element of an HTML document.

<head>

Description: Contains meta-information about the HTML document, such as title, character set, styles, and scripts.

<title>

Description: Sets the title of the HTML document, displayed in the browser's title bar or tab.
<meta name="viewport" content="width=device-width, initial-scale=1, shrink-to-fit=no"> -

Responsive meta tag to ensure proper rendering and touch zooming for all devices

<body>

Description: Contains the content of the HTML document, including text, images, links, and other elements.
<h1>, <h2>, ..., <h6>

Description: Defines headings of different levels, with <h1> being the highest and <h6> the lowest.
<p>

Description: Defines a paragraph.
<a>

Description: Creates hyperlinks to other web pages, files, or locations within the same document.
<img>

Description: Embeds an image in the document.
<ul>, <ol>, <li>

Description: Defines unordered (bulleted) and ordered (numbered) lists and list items.
<div>

Description: A container that groups and styles other HTML elements. Often used for layout purposes.
<span>

Description: A inline container used to apply styles or scripting to a specific portion of text.
<br>

Description: Inserts a line break within text.
<hr>

Description: Defines a horizontal line, often used to separate content.
<form>

Description: Creates a form to collect user input, which can include text fields, buttons, checkboxes, and more.
<input>

Description: Defines an input field within a form, such as text, password, checkbox, radio button, etc.
<label>

Description: Associates a label with a form element, improving accessibility and user experience.
<textarea>

Description: Creates a multiline text input field within a form.
<select>, <option>

Description: Defines a dropdown list within a form and its selectable options. */